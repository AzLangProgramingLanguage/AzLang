use std::fmt;

use crate::tokens::Token;
impl fmt::Display for Token {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Token::Identifier(name) => write!(f, "{}", name),
            Token::Number(num) => write!(f, "{}", num),
            Token::Float(num) => write!(f, "{}", num),
            Token::StringLiteral(string) => write!(f, "{}", string),
            Token::Operator(op) => write!(f, "{}", op),
            Token::Op(op) => write!(f, "{}", op),
            Token::LParen => write!(f, "("),
            Token::RParen => write!(f, ")"),
            Token::LBrace => write!(f, "{{"),
            Token::RBrace => write!(f, "}}"),
            Token::Indent => write!(f, "indent"),
            Token::Dedent => write!(f, "dedent"),
            Token::Newline => write!(f, "newline"),
            Token::Comment(comment) => write!(f, "comment: {}", comment),
            Token::Backtick => write!(f, "`"),
            Token::InterpolationStart => write!(f, "${{"),
            Token::InterpolationEnd => write!(f, "}}"),
            Token::Array => write!(f, "array"),
            Token::Object => write!(f, "object"),
            Token::Else => write!(f, "else"),
            Token::Import => write!(f, "import"),
            Token::This => write!(f, "this"),
            Token::Timer => write!(f, "timer"),
            Token::Sqrt => write!(f, "sqrt"),
            Token::End => write!(f, "end"),
            Token::ElseIf => write!(f, "else if"),
            Token::Drop => write!(f, "drop"),
            Token::Zig => write!(f, "zig"),
            Token::Match => write!(f, "match"),
            Token::Arrow => write!(f, "->"),
            Token::Loop => write!(f, "loop"),
            Token::Break => write!(f, "break"),
            Token::Print => write!(f, "print"),
            Token::ConvertString => write!(f, "convert string"),
            Token::Allocator => write!(f, "allocator"),
            Token::Continue => write!(f, "continue"),
            Token::Round => write!(f, "round"),
            Token::Floor => write!(f, "floor"),
            Token::Ceil => write!(f, "ceil"),
            Token::Method => write!(f, "method"),
            Token::True => write!(f, "true"),
            Token::False => write!(f, "false"),
            Token::Enum => write!(f, "enum"),
            Token::In => write!(f, "in"),
            Token::Semicolon => write!(f, ";"),
            Token::Colon => write!(f, ":"),
            Token::Comma => write!(f, ","),
            Token::ListStart => write!(f, "["),
            Token::ListEnd => write!(f, "]"),
            Token::Return => write!(f, "return"),
            Token::Eof => write!(f, "EOF"),
            Token::And => write!(f, "and"),
            Token::Or => write!(f, "or"),
            Token::DoubleAnd => write!(f, "&&"),
            Token::DoubleOr => write!(f, "||"),
            Token::NumberFn => write!(f, "number"),
            Token::RangeFn => write!(f, "range"),
            Token::Input => write!(f, "input"),
            Token::IntegerType => write!(f, "integer"),
            Token::NaturalType => write!(f, "natural"),
            Token::StringType => write!(f, "string"),
            Token::ZigConstString => write!(f, "zig const string"),
            Token::ZigConstArray => write!(f, "zig const array"),
            Token::ZigArray => write!(f, "zig array"),
            Token::ZigNatural => write!(f, "zig natural"),
            Token::ZigInteger => write!(f, "zig integer"),
            Token::Type => write!(f, "type"),
            Token::ZigString => write!(f, "zig string"),
            Token::CharType => write!(f, "char"),
            Token::BoolType => write!(f, "bool"),
            Token::FloatType => write!(f, "float"),
            Token::ZigFloat => write!(f, "zig float"),
            Token::BigIntegerType => write!(f, "big integer"),
            Token::LowIntegerType => write!(f, "low integer"),
            Token::MutableDecl => write!(f, "mutable"),
            Token::ConstantDecl => write!(f, "constant"),
            Token::FunctionDef => write!(f, "function"),
            Token::Conditional => write!(f, "conditional"),
            Token::Underscore => write!(f, "_"),
            Token::Void => write!(f, "void"),
            Token::Max => write!(f, "max"),
            Token::Min => write!(f, "min"),
            Token::Trim => write!(f, "trim"),
            Token::Mod => write!(f, "mod"),
            Token::Len => write!(f, "len"),
            Token::StrUpper => write!(f, "str upper"),
            Token::StrLower => write!(f, "str lower"),
            Token::StrReverse => write!(f, "str reverse"),
            Token::LastWord => write!(f, "last word"),
            Token::Sum => write!(f, "sum"),
            Token::Dot => write!(f, "."),
        }
    }
}
